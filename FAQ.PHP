<h3>2,Discuss Your Medications With Your Doctor</h3>
<div style="font-size:22px;">Roughly 40 percent of people over the age of 60 take at least five prescription drugs,
including medications they no longer need. Some medications -- like sleeping pills
-- should only be used short-term.</div><br>
<h3>3. Stop for the Right Reason</h3>
<div style="font-size:22px;">According to a report from CVS Caremark, half of patients taking maintenance
medications for chronic conditions will stop taking them within the first year 
of starting therapy. This "non-adherence" (not sticking to your treatment plan)
results in roughly a $300 million charge to health care costs per year. Non-adherence can lead to worsening of 
long-term health conditions, increased hospitalizations, and worse outcomes overall.</div>
<h3>4. Ask Your Doctor How to Best Stop Your Medication.</h3><br>
<div style="font-size:22px;">Some drugs can be stopped immediately without a special discontinuation schedule,
but many medications require a taper, which is a slow reduction in
dose or frequency over a longer period of time. Slow tapers help to
avoid disagreeable side effects or even withdrawals that you might 
experience if you stopped the drug quickly.
If you are stopping several medications, you may want to taper them 
one at a time to avoid added side effects -- always check with your doctor.</div>
<h3>5. Learn Your Medicine Side Effects if You Quickly Stop.</h3><br>
<div style="font-size:22px;">For example, abruptly stopping some selective serotonin reuptake inhibitors (SSRI) 
antidepressants prescribed to boost your mood -- like Paxil (paroxetine) or Zoloft 
(sertraline) -- can lead to a host of disturbing side effects, such as anxiety, dizziness, 
upset stomach, or fatigue. This is called antidepressant discontinuation syndrome. 
Not everyone experiences this antidepressant discontinuation syndrome, only about 1 
in 5 people. It is not usually dangerous, and typically subsides in a few days depending 
upon your drug, but there are ways to lessen its effect.

