<h1>Is it safe to take expired medications?</h1>

<div style="font-size:22px;">The U.S. Food and Drug Administration (FDA) recommends never taking drugs beyond their expiration 
date as it is risky with many unknown variables. For example, how your drug is stored before you receive it, 
chemical make-up, and original manufacturing date can all affect potency of a drug.

In 1963, a report was published that tied degraded tetracycline use 
with a form of renal tubular (kidney) damage known as "Fanconi Syndrome". However,
that formulation of tetracycline is no longer marketed in the U.S.1, 11, and many medical 
experts question the results of this case report.

Solid dosage forms, such as tablets and capsules, appear to be most stable past their 
expiration date. Drugs that exist in solution or as a reconstituted suspension, and that
require refrigeration (such as amoxicillin suspension), may not have the required potency 
if used when outdated. Loss of potency can be a major health concern, especially when treating
an infection with an antibiotic. Additionally, antibiotic resistance may occur with sub-potent medications.
